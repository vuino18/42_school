#include "libft.h"

char	*ft_strdup(const char *src)
{
	size_t		i;
	char		*dest;

	if (!src)
		return ((char *)0);
	i = 0;
	dest = malloc((ft_strlen(src) + 1));
	if (dest == NULL)
		return (NULL);
	while (src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
