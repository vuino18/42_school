#include "push_swap.h"

int	is_empty_or_spaces(char *s)
{
	int	i;

	if (!s || s[0] == '\0')
		return (1);
	i = 0;
	while (s[i])
	{
		if (s[i] != ' ')
			return (0);
		i++;
	}
	return (1);
}
